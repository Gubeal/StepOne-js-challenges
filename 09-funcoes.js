// Crie as seguintes funções:
let num1 = 10;
let num2 = 2;
// 1- Função de soma, com o nome ‘sum', que retorna a soma de dois números.
function sum(num1, num2) {
    return num1 + num2;
}
console.log(`Soma de ${num1} e ${num2}: ${sum(num1, num2)}`);

// 2- Função de subtração, com o nome ‘subtract’, que retorna a subtração de dois números
function subtract(num1, num2) {
    return num1 - num2;
}
console.log(`Subtração de ${num1} e ${num2}: ${subtract(num1, num2)}`);

// 3- Função de multiplicação, com o nome ‘multiplication, que retorna 
// a multiplicação de dois números
function multiplication(num1, num2) {
    return num1 * num2;
}
console.log(`Multiplicação de ${num1} e ${num2}: ${multiplication(num1, num2)}`);


// 4- Função de divisão, com o nome 'division', que retorna a divisão do primeiro 
//número pelo outro

function division(num1, num2) {
    return num1 / num2;
}
console.log(`Divisão de ${num1} por ${num2}: ${division(num1, num2)}`);
